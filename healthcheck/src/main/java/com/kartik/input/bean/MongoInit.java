package com.kartik.input.bean;

public class MongoInit {

	private String host;
	private int port;
	private String replicaSet;
	private String userName;
	private String passWord;
	private String dbName;
	private String authMechanisum;
	/**
	 * @return the host
	 */
	public String getHost() {
		return host;
	}
	/**
	 * @param host the host to set
	 */
	public void setHost(String host) {
		this.host = host;
	}
	/**
	 * @return the port
	 */
	public int getPort() {
		return port;
	}
	/**
	 * @param port the port to set
	 */
	public void setPort(int port) {
		this.port = port;
	}
	/**
	 * @return the replicaSet
	 */
	public String getReplicaSet() {
		return replicaSet;
	}
	/**
	 * @param replicaSet the replicaSet to set
	 */
	public void setReplicaSet(String replicaSet) {
		this.replicaSet = replicaSet;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the passWord
	 */
	public String getPassWord() {
		return passWord;
	}
	/**
	 * @param passWord the passWord to set
	 */
	public void setPassWord(String passWord) {
		this.passWord = passWord;
	}
	/**
	 * @return the dbName
	 */
	public String getDbName() {
		return dbName;
	}
	/**
	 * @param dbName the dbName to set
	 */
	public void setDbName(String dbName) {
		this.dbName = dbName;
	}
	/**
	 * @return the authMechanisum
	 */
	public String getAuthMechanisum() {
		return authMechanisum;
	}
	/**
	 * @param authMechanisum the authMechanisum to set
	 */
	public void setAuthMechanisum(String authMechanisum) {
		this.authMechanisum = authMechanisum;
	}
	
}
